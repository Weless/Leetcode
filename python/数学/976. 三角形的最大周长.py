from typing import List
class Solution:
    def largestPerimeter(self, A: List[int]) -> int:
        def helper(a,b,c):
            if a + b > c and a + c > b and b + c > a:
                return True
            return False
        if len(A) < 3 :
            return 0
        A.sort(reverse=True)
        i = 0
        while i < len(A)-2:
            if helper(A[i],A[i+1],A[i+2]):
                return sum([A[i],A[i+1],A[i+2]])
            i+=1
        return 0

